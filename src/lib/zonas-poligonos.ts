export function getPoligono(zonaId: string): [number, number][] | null {
  return POLIGONOS[zonaId] ?? null
}

export function getBoundsFromPoligono(coords: [number, number][]): { north: number; south: number; east: number; west: number } {
  let north = -90, south = 90, east = -180, west = 180
  for (const [lat, lng] of coords) {
    if (lat > north) north = lat
    if (lat < south) south = lat
    if (lng > east) east = lng
    if (lng < west) west = lng
  }
  return { north, south, east, west }
}

export function isPointInPolygon(lat: number, lng: number, polygon: [number, number][]): boolean {
  let inside = false
  for (let i = 0, j = polygon.length - 1; i < polygon.length; j = i++) {
    const [yi, xi] = polygon[i]
    const [yj, xj] = polygon[j]
    if (((yi > lat) !== (yj > lat)) && (lng < (xj - xi) * (lat - yi) / (yj - yi) + xi)) {
      inside = !inside
    }
  }
  return inside
}
